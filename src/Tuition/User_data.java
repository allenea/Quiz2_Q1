package Tuition;

public class User_data {
	
	private double cost_of_college;
	private double perc_increase;
	private double apr;
	private double term;

	
	public double getCost_of_college() {
		return cost_of_college;
	}
	public void setCost_of_college(double cost_of_college) {
		this.cost_of_college = cost_of_college;
	}
	
	
	public double getPerc_increase() {
		return perc_increase;
	}
	public void setPerc_increase(double perc_increase) {
		this.perc_increase = perc_increase;
	}
	
	
	public double getApr() {
		return apr;
	}
	public void setApr(double apr) {
		this.apr = apr;
	}
	
	
	public double getTerm() {
		return term;
	}
	public void setTerm(double term) {
		this.term = term;
	}
	
	
}